	,DEPT_NO CHAR(4)
	,FROM_DATE DATE NOT NULL
	,TO_DATE DATE NOT NULL
	,CONSTRAINT PK_DEPT_EMP1 PRIMARY KEY(EMP_NO,DEPT_NO)
